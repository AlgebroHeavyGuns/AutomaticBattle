    private void clearCapitulos(){
        mision1.setText("NO DISPONIBLE");
        mision2.setText("NO DISPONIBLE");
        mision3.setText("NO DISPONIBLE");
        mision4.setText("NO DISPONIBLE");
        mision5.setText("NO DISPONIBLE");
    }
    
    private void actualizarCombates(){
        clearCapitulos();
        listaCombates = new ArrayList<>();
        for(int i=0; i<5; i++)
            listaCombates.add(ProductorCombate.getInstance().getCombateNivel(i+inicial));
        int j=0;
        for(CombateInfo CI : listaCombates){
            fijarCapitulo(CI, j, inicial+j);
            ++j;
        }
            
    }
    
    private void fijarCapitulo(CombateInfo CI, int num, int combindice){
        switch(num){
            case 0:
                mision1.setEnabled(CI!=null);
                if(CI!=null){
                    mision1.setText(combindice + ". " + CI.getNombre());
                    mision1.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/icons/" +CI.getImagen())));
                }else
                    mision1.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/icons/libro.png")));
                break;
            case 1:
                mision2.setEnabled(CI!=null);
                if(CI!=null){
                    mision2.setText(combindice + ". " + CI.getNombre());
                    mision2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/icons/" +CI.getImagen())));
                }else
                    mision2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/icons/libro.png")));
                break;
            case 2:
                mision3.setEnabled(CI!=null);
                if(CI!=null){
                    mision3.setText(combindice + ". " + CI.getNombre());
                    mision3.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/icons/" +CI.getImagen())));
                }else
                    mision3.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/icons/libro.png")));
                break;    
            case 3:
                mision4.setEnabled(CI!=null);
                if(CI!=null){
                    mision4.setText(combindice + ". " + CI.getNombre());
                    mision4.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/icons/" +CI.getImagen())));
                }else
                    mision4.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/icons/libro.png")));
                break;
            case 4:
                mision5.setEnabled(CI!=null);
                if(CI!=null){
                    mision5.setText(combindice + ". " + CI.getNombre());
                    mision5.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/icons/" +CI.getImagen())));
                }else
                    mision5.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/icons/libro.png")));
                break;    
                
        }
    }

